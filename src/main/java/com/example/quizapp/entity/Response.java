package com.example.quizapp.entity;

public class Response {
    private int id;
    private String response;

    // Getter for id
    public int getId() {
        return id;
    }

    // Setter for id
    public void setId(int id) {
        this.id = id;
    }

    // Getter for response
    public String getResponse() {
        return response;
    }

    // Setter for response
    public void setResponse(String response) {
        this.response = response;
    }
}